//1) 반복 while를 이용해서 1~ 5 까지 출력 해보자.   1,2,3,4,5 
var count = 1 ;                             // count =1

while(count <= 5){                       // (1 <=5 )=true, (2<=5)t  , (3<=5)t, (4<=5)t ,   (5<=5)t, (6<=5)f
  console.log("count :" + count);     //"count : 1", "count : 2" , "count : 3" , "count : 4","count : 5"
  count= count + 1 ;   // count++;    // count = 2 , count = 3 , count = 4 ,count = 5 ,count = 6
}
 console.log("========> count :" + count);

//2) 반복 while를 이용해서 10~1 까지 출력 해보자.   10, 9 ,8, 7 ~~~

var count01 = 10 ;                            // count =1

while(count01 >=  8 ){                       // (1 <=5 )=true, (2<=5)t  , (3<=5)t, (4<=5)t ,   (5<=5)t, (6<=5)f
  console.log("count :" + count01);     //"count : 1", "count : 2" , "count : 3" , "count : 4","count : 5"
  count01= count01 - 1 ;   // count++;    // count = 2 , count = 3 , count = 4 ,count = 5 ,count = 6
}
 console.log("========> count01 :" + count01);




